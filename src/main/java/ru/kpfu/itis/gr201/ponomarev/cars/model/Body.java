package ru.kpfu.itis.gr201.ponomarev.cars.model;

public class Body {
    private int id;
    private String body;

    public Body(int id, String body) {
        this.id = id;
        this.body = body;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
